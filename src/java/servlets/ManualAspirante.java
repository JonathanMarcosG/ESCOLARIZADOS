package servlets;

import PDF.CreateMan2;
import java.io.ByteArrayOutputStream;
import java.io.IOException;
import java.io.OutputStream;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 *
 * @author Chris
 */
public class ManualAspirante extends HttpServlet {

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        CreateMan2 man = new CreateMan2();
        ByteArrayOutputStream baos=man.create(getServletContext());
        sendDocument(response, baos);
    }
    
    public void sendDocument(HttpServletResponse response, ByteArrayOutputStream obj) throws IOException{
        response.setHeader("Expires", "0");
            response.setHeader("Cache-Control", "must-revalidate, post-check=0, pre-check=0");
            response.setHeader("Pragma", "public");
            response.setContentType("application/pdf");
            response.setContentLength(obj.size());
        try (OutputStream os = response.getOutputStream()) {
            obj.writeTo(os);
            os.flush();
            os.close();
        }
    }

}
